import "dotenv/config";
import { readFileSync } from "fs";
import { join } from "path";
import { pool } from "./src/db";

async function runMigration() {
  const client = await pool.connect();
  
  try {
    console.log("üìñ Reading migration file...");
    const migrationPath = join(__dirname, "migrations", "0001_add_multi_tenancy.sql");
    const migrationSQL = readFileSync(migrationPath, "utf-8");

    console.log("üöÄ Executing migration...");
    console.log("‚ö†Ô∏è  This will add businesses table and businessId columns to all tables.");
    console.log("‚ö†Ô∏è  Existing data will be assigned to a default business.\n");

    // Execute the entire migration as one transaction
    await client.query("BEGIN");
    
    try {
      // Execute the migration SQL
      await client.query(migrationSQL);
      
      await client.query("COMMIT");
      console.log("\n‚úÖ Migration completed successfully!");
      console.log("‚úÖ Businesses table created");
      console.log("‚úÖ Default business created for existing data");
      console.log("‚úÖ businessId columns added to all relevant tables");
      console.log("‚úÖ Indexes created");
    } catch (error) {
      await client.query("ROLLBACK");
      throw error;
    }
    
    process.exit(0);
  } catch (error) {
    console.error("\n‚ùå Migration failed:", error);
    if (error instanceof Error) {
      console.error("Error message:", error.message);
      console.error("Error stack:", error.stack);
    }
    process.exit(1);
  } finally {
    client.release();
    await pool.end();
  }
}

runMigration();

