import "dotenv/config";
import { readFileSync } from "fs";
import { join } from "path";
import { pool } from "./src/db";

async function runMigration() {
  const client = await pool.connect();
  
  try {
    console.log("üìñ Reading migration file...");
    const migrationPath = join(__dirname, "migrations", "0016_make_email_history_customer_id_nullable.sql");
    const migrationSQL = readFileSync(migrationPath, "utf-8");

    console.log("üöÄ Executing migration...");
    console.log("‚ö†Ô∏è  This will make customer_id nullable in email_history table.\n");

    // Execute the entire migration as one transaction
    await client.query("BEGIN");
    
    try {
      // Execute the migration SQL
      await client.query(migrationSQL);
      
      await client.query("COMMIT");
      console.log("\n‚úÖ Migration completed successfully!");
      console.log("‚úÖ customer_id column is now nullable in email_history table");
      console.log("‚úÖ Emails can now be logged even without customer records");
    } catch (error) {
      await client.query("ROLLBACK");
      throw error;
    }
    
    process.exit(0);
  } catch (error) {
    console.error("\n‚ùå Migration failed:", error);
    if (error instanceof Error) {
      console.error("Error message:", error.message);
      console.error("Error stack:", error.stack);
    }
    process.exit(1);
  } finally {
    client.release();
    await pool.end();
  }
}

runMigration();






